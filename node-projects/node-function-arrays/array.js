// declare a simple array
let nameArray = ['John', 'Sam', 'Max'];
console.log(nameArray);


// push a new value to the array
nameArray.push('Roy');
console.log(nameArray);

// remove the value from the array
nameArray.pop('Roy');
console.log(nameArray);

// update the value at a particular index
nameArray[1] = "Jack";
console.log(nameArray);

